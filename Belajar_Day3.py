"""Belajar tipe data"""

#tipe data integer
#integer (int) adalah tipe data bilangan bulat
a = 10
print ("nilai a = ", a," bertipe : ", type(a))

#tipe data float
#ipe data float adalah tipe data bilangan desimal
b = 10.2
print ("nilai b = ", b," bertipe : ", type(b))

#tipe data string
#ipe data string adalah tipe data huruf
#tipe data string ketika dimasukkan kedalam variabel harus ditambahkan tanda petik 2 ("")
c = "10.2"
d = "aku seorang programer"
print ("nilai c = ", c," bertipe : ", type(c))
print ("nilai d = ", d," bertipe : ", type(d))

#tipe data boolean
#ipe data boolean adalah tipe data dengan nilai True dan false
e = False
f = True
print ("nilai e = ", e," bertipe : ", type(e))
print ("nilai f = ", f," bertipe : ", type(f))

